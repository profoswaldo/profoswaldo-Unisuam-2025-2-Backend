<?php
// Definindo duas variáveis
$a = 10;
$b = 3;

echo "<h2>Exemplo de Operadores Aritméticos em PHP</h2>";

echo "a = " . $a . "<br>";
echo "b = " . $b . "<br><br>";

// Adição (+)
echo $a . " + " . $b . " = " . ($a + $b) . "<br>";

// Subtração (-)
echo $a . " - " . $b . " = " . ($a - $b) . "<br>";

// Multiplicação (*)
echo $a . " * " . $b . " = " . ($a * $b) . "<br>";

// Divisão (/)
echo $a . " / " . $b . " = " . ($a / $b) . "<br>";

// Módulo ou resto (%)
echo $a . " % " . $b . " = " . ($a % $b) . "<br>";

// Exponenciação (**)
echo $a . " ** " . $b . " = " . ($a ** $b) . "<br>";
?>
